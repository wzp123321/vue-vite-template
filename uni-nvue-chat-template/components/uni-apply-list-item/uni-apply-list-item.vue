<template>
	<!-- #ifdef APP-NVUE -->
	<cell>
		<!-- #endif -->
		<view :class="disabled ? 'uni-list-item--disabled' : ''" :hover-class="disabled || showSwitch ? '' : 'uni-list-item--hover'"
		 class="uni-list-item" @click="onClick">
			<view class="uni-list-item__container" :style="containerStyle" :class="{'uni-list-item--first':isFirstChild}">
				<view v-if="thumb" class="uni-list-item__icon">
					<image :src="thumb" class="uni-list-item__icon-img" :style="thumbStyle" />
				</view>
				<view v-else-if="showExtraIcon" class="uni-list-item__icon">
					<uni-icons :color="extraIcon.color" :size="extraIcon.size" :type="extraIcon.type" class="uni-icon-wrapper" />
				</view>
				<view class="uni-list-item__content" :style="desc ? 'flex-direction: row;align-items: center;' : ''">
					<slot />
					<text class="uni-list-item__content-title" :style="textStyle">{{ title }}</text>
					<text v-if="desc" :style="descStyle" style="word-break: break-word; width: 65%;">{{desc}}</text>
					<text v-if="note" class="uni-list-item__content-note">{{ note }}</text>
				</view>
				<view class="uni-list-item__extra">
					
					<!--  #ifdef  APP-NVUE -->
					<button @click="onClickApply(1,listIndex)" size="mini" type="default" v-if="applyStatus == 0" style="background-color: #ebebeb;padding-left: 12px;padding-right: 12px;padding-top: 6px;padding-bottom: 6px;border-color: #ebebeb;" ><text style="color:#04BE02;font-size: 14px;">接受</text></button>
					<!--  #endif -->
					
					<!--  #ifndef  APP-NVUE -->
					<button class="wxappButton" @click="onClickApply(1,listIndex)" size="mini" type="default" v-if="applyStatus == 0" style="background-color: #ebebeb;padding-left: 12px;padding-right: 12px;border-color: #ebebeb;" ><text style="color:#04BE02;font-size: 14px;">接受</text></button>
					<!--  #endif -->
					
			 
					<text v-if="applyStatus == 1" class="uni-list-item__extra-text">已同意</text>
					<text v-if="applyStatus == 2" class="uni-list-item__extra-text">已拒绝</text>
					 
					<uni-badge v-if="showBadge" :type="badgeType" :text="badgeText" />
					<switch v-if="showSwitch" :disabled="disabled" :checked="switchChecked" @change="onSwitchChange" />
					<slot name="right"></slot>
					<uni-icons v-if="showArrow" :size="20" class="uni-icon-wrapper" color="#bbb" type="arrowright" />
				</view>
			</view>
		</view>
		<!-- #ifdef APP-NVUE -->
	</cell>
	<!-- #endif -->
</template>

<script>
	import uniIcons from '../uni-icons/uni-icons.vue'
	import uniBadge from '../uni-badge/uni-badge.vue'

	/**
	 * ListItem 列表子组件
	 * @description 列表子组件
	 * @tutorial https://ext.dcloud.net.cn/plugin?id=24
	 * @property {String} title 标题
	 * @property {String} note 描述
	 * @property {String} thumb 左侧缩略图，若thumb有值，则不会显示扩展图标
	 * @property {String} badgeText 数字角标内容
	 * @property {String} badgeType 数字角标类型，参考[uni-icons](https://ext.dcloud.net.cn/plugin?id=21)
	 * @property {String} rightText 右侧文字内容
	 * @property {Boolean} disabled = [true|false]是否禁用
	 * @property {Boolean} showArrow = [true|false] 是否显示箭头图标
	 * @property {Boolean} showBadge = [true|false] 是否显示数字角标
	 * @property {Boolean} showSwitch = [true|false] 是否显示Switch
	 * @property {Boolean} switchChecked = [true|false] Switch是否被选中
	 * @property {Boolean} showExtraIcon = [true|false] 左侧是否显示扩展图标
	 * @property {Boolean} scrollY = [true|false] 允许纵向滚动，需要显式的设置其宽高
	 * @property {Object} extraIcon 扩展图标参数，格式为 {color: '#4cd964',size: '22',type: 'spinner'}
	 * @event {Function} click 点击 uniListItem 触发事件
	 * @event {Function} switchChange 点击切换 Switch 时触发
	 */
	export default {
		name: 'UniListItem',
		components: {
			uniIcons,
			uniBadge
		},
		props: {
			title: {
				type: String,
				default: ''
			}, // 列表标题
			note: {
				type: String,
				default: ''
			}, // 列表描述
			disabled: {
				// 是否禁用
				type: [Boolean, String],
				default: false
			},
			showArrow: {
				// 是否显示箭头
				type: [Boolean, String],
				default: true
			},
			showBadge: {
				// 是否显示数字角标
				type: [Boolean, String],
				default: false
			},
			showSwitch: {
				// 是否显示Switch
				type: [Boolean, String],
				default: false
			},
			switchChecked: {
				// Switch是否被选中
				type: [Boolean, String],
				default: false
			},
			badgeText: {
				// badge内容
				type: String,
				default: ''
			},
			badgeType: {
				// badge类型
				type: String,
				default: 'success'
			},
			rightText: {
				// 右侧文字内容
				type: String,
				default: ''
			},
			textStyle: {
				// 文字样式
				type: String,
				default: ''
			},
			containerStyle: {
				// 样式
				type: String,
				default: ''
			},
			desc: {
				
				type: String,
				default: ''
			},
			descStyle: {
				// 样式
				type: String,
				default: ''
			},
			thumb: {
				// 缩略图
				type: String,
				default: ''
			},
			thumbStyle:{
				type: String,
				default: ''
			},
			listIndex:{ 
				type: [String,Number],
				default: ''
			},
			showExtraIcon: {
				// 是否显示扩展图标
				type: [Boolean, String],
				default: false
			},
			applyStatus:{
				type: Number,
				default: 0
			},
			extraIcon: {
				type: Object,
				default () {
					return {
						type: 'contact',
						color: '#000000',
						size: 20
					}
				}
			}
		},
		inject: ['list'],
		data() {
			return {
				isFirstChild: false
			}
		},
		mounted() {
			if (this.list.firstChildAppend != undefined && !this.list.firstChildAppend) {
				this.list.firstChildAppend = true
				this.isFirstChild = true
			}
		},
		methods: {
			onClick() {
				this.$emit('click')
			},
			onSwitchChange(e) {
				this.$emit('switchChange', e.detail)
			},
			onClickApply(status,listIndex) {
				this.$emit("clickApply",{
					status,
					listIndex
				});
			}, 
			
		}
	}
</script>

